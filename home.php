    <div class="freq-title">Frequently Asked Questions for First-Time OFWs</div>
    <div class="freq-space">
      <div class="freq-box">
        <h3 class="freq-sub-title">Frequently Asked Questions for First-Time OFWs</h3>

        <div class="faq-item">
          <div class="faq-question">
            <span>What documents do I need to apply as an OFW?</span>
            <span class="arrow">^</span>
          </div>
          <div class="faq-answer">
            As a first-time OFW, you'll need a valid passport with at least 6 months validity,
            professional/technical qualifications (if applicable), employment contract, work visa/permit,
            medical certificate, NBI clearance, birth certificate, and OWWA membership.
            Additional documents may be required depending on your destination country.
          </div>
        </div>

        <div class="faq-item">
          <div class="faq-question">
            <span>How much does it cost to apply through PMDL?</span>
            <span class="arrow">^</span>
          </div>
          <div class="faq-answer">
            PMDL adheres to the POEA guidelines on placement fees. The specific amount varies depending on the job and
            destination country. We provide a transparent breakdown of all costs during your application process.
            Remember that legitimate agencies should not charge excessive fees.
          </div>
        </div>

        <div class="faq-item">
          <div class="faq-question">
            <span>How long does the entire application process take?</span>
            <span class="arrow">^</span>
          </div>
          <div class="faq-answer">
            The timeline varies depending on the employer, job position, and destination country. Typically, the process
            takes 2-4 months from initial application to deployment. This includes document processing, visa
            application, medical examination, and pre-departure orientation.
          </div>
        </div>

        <div class="faq-item">
          <div class="faq-question">
            <span>Do I need to attend the PDOS (Pre-Departure Orientation Seminar)?</span>
            <span class="arrow">^</span>
          </div>
          <div class="faq-answer">
            Yes, all first-time OFWs are required by Philippine law to attend the PDOS. This seminar provides essential
            information about working overseas, including cultural differences, employment rights, and what to expect in
            your destination country.
          </div>
        </div>

        <div class="faq-item">
          <div class="faq-question">
            <span>Can PMDL help me if I encounter problems with my employer abroad?</span>
            <span class="arrow">^</span>
          </div>
          <div class="faq-answer">
            Yes, PMDL provides ongoing support to all deployed workers. We maintain communication channels for
            addressing concerns and work closely with Philippine overseas labor offices to ensure your rights are
            protected. We can assist with contract issues, workplace concerns, or emergency situations.
          </div>
        </div>

        <div class="faq-item">
          <div class="faq-question">
            <span>What skills or qualifications do I need to work abroad?</span>
            <span class="arrow">^</span>
          </div>
          <div class="faq-answer">
            Requirements vary by position and country. Common requirements include relevant work experience, language
            proficiency (especially for certain countries), technical skills, and sometimes specific certifications.
            PMDL can help assess your qualifications and recommend suitable positions.
          </div>
        </div>

        <div class="faq-item">
          <div class="faq-question">
            <span>Is it safe to work overseas as an OFW?</span>
            <span class="arrow">^</span>
          </div>
          <div class="faq-answer">
            Working overseas involves certain risks, but PMDL partners only with legitimate and vetted employers who
            provide safe working conditions. We conduct thorough background checks on all employers and provide
            pre-departure orientation to prepare you for your overseas experience.
          </div>
        </div>

